/**
* @brief This function handles EXTI line[15:10] interrupts.
*/
void EXTI15_10_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI15_10_IRQn 0 */
	Temperatue = Set_temp();
	st = HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_11);
	bt1 = HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_12);
	bt2 = HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_14);
	if (st==1){
		HAL_TIM_Base_Stop(&htim2);
	}
	else HAL_TIM_Base_Start_IT(&htim2);
	if (HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_11)==1){
		if ((HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_12)==0)){
			Num1++;
		}
		if(HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_14)==0){
			Num2++;
		}
	}
		if((HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_11)==1)&&(HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_12)==1)&&(HAL_GPIO_ReadPin(GPIOD,GPIO_PIN_14)==1)){
			Num1=0;
			Num2=0;
		}
		int32_t a=5680000;
		while(a--);
  /* USER CODE END EXTI15_10_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_11);
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_12);
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_14);
  /* USER CODE BEGIN EXTI15_10_IRQn 1 */

  /* USER CODE END EXTI15_10_IRQn 1 */
}

